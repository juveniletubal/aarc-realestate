        $(document).ready(function() {
            // ---------- CARDS ----------
            $(".card-img-top").each(function() {
                var img = $(this);
                var skeleton = img.siblings(".cardImageSkeleton");

                // hide actual image until loaded
                img.addClass("d-none");

                var tempImg = new Image();
                tempImg.src = img.attr("src");
                tempImg.onload = function() {
                    img.removeClass("d-none");
                    skeleton.hide();
                };
            });

            // Same for title, text, button (simulate API call or real AJAX)
            $(".card-title, .card-text, .read-more-btn").each(function() {
                var el = $(this);
                var skeleton = el.siblings(".skeleton");
                // If content already has text (server-rendered), just hide skeleton immediately
                if (el.text().trim() !== "") {
                    el.removeClass("d-none");
                    skeleton.hide();
                }
            });

            // ---------- MODAL ----------
            $(".read-more-btn").on("click", function() {
                var modal = $($(this).data("target"));
                var title = $(this).data("title");
                var img = $(this).data("img");
                var desc = $(this).data("desc");

                // Reset modal
                modal.find(".modal-title").addClass("d-none").text("");
                modal.find(".cardTitleSkeleton").show();

                modal.find(".modalImage").addClass("d-none").attr("src", "");
                modal.find(".cardImageSkeleton").show();

                modal.find(".modalDescription").addClass("d-none").html("");
                modal.find(".cardTextSkeleton").show();

                // Load title immediately (if available)
                if (title) {
                    modal.find(".modal-title").text(title).removeClass("d-none");
                    modal.find(".cardTitleSkeleton").hide();
                }

                // Load image (real-time, waits for download)
                var tempImg = new Image();
                tempImg.src = img;
                tempImg.onload = function() {
                    modal.find(".modalImage").attr("src", img).removeClass("d-none");
                    modal.find(".cardImageSkeleton").hide();
                };

                // Load description after ensuring content is available
                if (desc) {
                    modal.find(".modalDescription").html(desc).removeClass("d-none");
                    modal.find(".cardTextSkeleton").hide();
                }
            });
        });
    </script>

